'''
In Python, comparison operators are used to compare values and 
return a boolean value (True or False) based on whether the comparison is 
true or false. The following are the comparison operators in Python:

== (equal to): This operator returns True if both operands are equal.

!= (not equal to): This operator returns True if both operands are not equal.

> (greater than): This operator returns True if the left operand is greater than the right operand.

< (less than): This operator returns True if the left operand is less than the right operand.

>= (greater than or equal to): This operator returns True if the left operand is greater than or equal to the right operand.

<= (less than or equal to): This operator returns True if the left operand is less than or equal to the right operand.

'''

x = 5
y = 10

print(x == y)    # False
print(x != y)    # True
print(x > y)     # False
print(x < y)     # True
print(x >= y)    # False
print(x <= y)    # True
